fun main(args: Array<String>) {
    println("Hello there kotlin")
    println(20+30)

    //kotlin operators
    val num1 = 101.99F
    val num2 = 100.50F
    var op: Float

    op = num1 + num2
    println("Addition: " + op)

    op = num1 - num2
    println("Subtraction: " +op)

    op = num1 * num2
    println("Multiplication: " +op)

    op = num1 / num2
    println("Division: " +op)

    op = num1 % num2
    println("Modulus: " +op)
}

